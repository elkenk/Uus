package ee.ken.kalkulaator;

public class SkoorElud {
    int skoor;
    int elud;

    public SkoorElud(int skoor, int elud) {
        this.skoor = skoor;
        this.elud = elud;
    }

    public int getSkoor() {
        return skoor;
    }

    public int getElud() {
        return elud;
    }


}
